export const getIcon = (extension)=>{

    let icon = ''

    switch (extension) {
        case 'folder':
            icon = 'folder'
            break;
        case 'doc':
            icon = 'file-word'
            break;
        case 'csv':
            icon = 'file-word'
            break;
        case 'jpg':
            icon = 'file-word'
            break;
        case 'txt':
            icon = 'file-word'
            break;
        case 'ppt':
            icon = 'file-word'
            break;
        case 'odt':
            icon = 'file-word'
            break;
        case 'pdf':
            icon = 'file-word'
            break;
        case 'zip':
            icon = 'file-word'
            break;
        case 'rar':
            icon = 'file-word'
            break;
        case 'exe':
            icon = 'file-word'
            break;
        case 'svg':
            icon = 'file-word'
            break;
        case 'mp3':
            icon = 'file-word'
            break;
        case 'mp4':
            icon = 'file-word'
            break;
    }

    return icon
}